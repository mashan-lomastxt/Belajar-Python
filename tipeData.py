# tipe data Boolean
print(True)

# tipe data String
print("Belajar python itu seru...!")
print("Python itu Mudah...")

# tipe data Integer
print(22)

# tipe data Float
print(3.14)

# tipe data Hexadecimal
print(0xFF)

# tipe data Complex
print(1j)

# tipe data List
print([1,2,3,4,5])
print(["Belajar", "Python", "Itu", "Seru"])

# tipe data Tuples
print((1,2,3,4,5))
print(("satu", "dua", "tiga", "empat", "lima"))

# tipe data Dictionary
print({1:"satu", 2:"dua", 3:"tiga", 4:"empat", 5:"lima"})

