# Contoh penggunaan kondisi elif

hari_ini = "Jumat"

if(hari_ini) == "Senin":
    print("Saya berencana berpuasa")
elif(hari_ini) == "Selasa":
    print("Saya berangkat ke Kampus")
elif(hari_ini) == "Rabu":
    print("Saya ada kumpul Komunitas")
elif(hari_ini) == "Kamis":
    print("Saya berencana berpuasa")
elif(hari_ini) == "Jumat":
    print("Saya berangkat ke Masjid")
elif(hari_ini) == "Sabtu":
    print("Saya bermain dengan teman")
elif(hari_ini) == "Minggu":
    print("Saya tidak berangkat ke Kampus")